export const t = (lang, key) => STRINGS[lang]?.[key] ?? STRINGS.bn[key] ?? key;

/* Pick a localized field on a data object: tr(item, 'name', lang) returns
   item.name_en when lang==='en' (if it exists), else item.name. */
export const tr = (item, field, lang) => {
  if (!item) return '';
  if (lang === 'en' && item[`${field}_en`] != null) return item[`${field}_en`];
  return item[field];
};

